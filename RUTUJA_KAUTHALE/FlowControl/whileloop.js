let P=0;
console.log("Print 0 to 10 NUMBERS");
while(P<=10)
{
    console.log(P);
    P++;
}
P=0;

//   Even number print
console.log("Even NUMBERS");
 while(P<=10){
    if(P%2==0)
    {
        console.log(P);
    }
    P++;
    
 }
 P=0;
//   odd number print
console.log("ODD NUMBERS");
while(P<=10){
    if(P%2==1)
    {
        console.log(P);
    }
    P++;
    
 }