
  let day = "Monday";

// switch case
day = 'Monday';
switch(day){

     case 'Monday':{
        console.log("I am a Monday");
        break;
     }

     case 'wednesday':{
      console.log("I am a wednesday");
     break;
   }

     case 'Friday':{
        console.log("I am a Friday");
        break;
     }

     case 'Sunday':{
        console.log("I am  a Sunday");
        break;
     }

     default:{
        console.log("I am a default case");
        break;
     }

}

